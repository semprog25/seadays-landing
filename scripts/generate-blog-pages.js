#!/usr/bin/env node
/**
 * generate-blog-pages.js
 *
 * Fetches articles from https://seadays.com/api/blog, generates static HTML
 * from blog-article.html template, writes /blog/{slug}/index.html, and updates sitemap.xml.
 * Writes a redirect page at /blog/{slug}.html for backward compatibility.
 * Only regenerates pages when article updated_at changed; skips manual files.
 *
 * API failure: if the blog API is unavailable after retries (3 attempts, 1s delay),
 * logs an error, skips generation (no file writes, no sitemap/manifest overwrite),
 * and exits 0 so CI (e.g. GitHub Actions) does not fail.
 *
 * Usage: node scripts/generate-blog-pages.js
 */
'use strict';

const https = require('https');
const fs = require('fs');
const path = require('path');

const API_BASE = 'https://seadays.com/api/blog';
const BASE_URL = 'https://seadays.com';
const SITE_NAME = 'SeaDays';
const GENERATED_MARKER = '<!-- generated by generate-blog-pages.js -->';

const repoRoot = path.join(__dirname, '..');
const blogDir = path.join(repoRoot, 'blog');
const templatePath = path.join(repoRoot, 'blog-article.html');
const manifestPath = path.join(repoRoot, '.blog-pages-manifest.json');

function escapeHtml(s) {
  if (s == null || s === '') return '';
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function escapeXml(s) {
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&apos;');
}

function slugify(title) {
  if (!title || typeof title !== 'string') return '';
  return title
    .toLowerCase()
    .trim()
    .replace(/[^\w\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '') || '';
}

function toUpdatedAtMs(article) {
  const raw = article.updated_at ?? article.updatedAt ?? article.timestamp ?? article.published_at ?? article.publishedAt ?? article.createdAt ?? 0;
  if (typeof raw === 'number') return raw < 10000000000 ? raw * 1000 : raw;
  const parsed = Date.parse(raw);
  return Number.isFinite(parsed) ? parsed : 0;
}

function toPublishedIso(article) {
  const raw = article.published_at ?? article.publishedAt ?? article.timestamp ?? article.created_at ?? article.createdAt;
  if (!raw) return '';
  const ms = typeof raw === 'number' ? (raw < 10000000000 ? raw * 1000 : raw) : Date.parse(raw);
  if (!Number.isFinite(ms)) return '';
  return new Date(ms).toISOString().split('T')[0];
}

function formatDisplayDate(ts) {
  if (ts == null) return '';
  const ms = typeof ts === 'number' ? (ts < 10000000000 ? ts * 1000 : ts) : Date.parse(ts);
  if (!Number.isFinite(ms)) return '';
  return new Date(ms).toLocaleDateString('en-US', { year: 'numeric', month: 'short', day: 'numeric' });
}

function getBodyHtml(article) {
  const content = article.content;
  if (content && typeof content === 'string' && content.trim()) {
    if (content.trim().startsWith('<')) return content;
    return formatParagraphsToHtml(content);
  }
  const raw = article.structuredContent;
  if (!raw) return '<p>No content available.</p>';
  let parsed;
  try {
    parsed = typeof raw === 'string' ? JSON.parse(raw) : raw;
  } catch {
    return '<p>No content available.</p>';
  }
  if (parsed?.version !== 2 || !Array.isArray(parsed.sections)) return '<p>No content available.</p>';
  const parts = [];
  for (const section of parsed.sections) {
    const heading = (section.heading || '').trim();
    const level = section.headingLevel === 'h3' ? 'h3' : 'h2';
    if (heading) parts.push(`<${level}>${escapeHtml(heading)}</${level}>`);
    for (const block of section.blocks || []) {
      if (block.type === 'paragraph' && block.content) parts.push(formatParagraphsToHtml(block.content));
      else if (block.type === 'heading' && block.content) parts.push('<h2>' + escapeHtml(block.content) + '</h2>');
      else if (block.type === 'image' && block.images?.length) {
        for (const img of block.images) {
          if (img?.url && !String(img.url).startsWith('data:image')) {
            const alt = escapeHtml(img.alt || img.caption || '');
            parts.push(`<figure><img src="${escapeHtml(img.url)}" alt="${alt}" loading="lazy" decoding="async"></figure>`);
          }
        }
      } else if (block.type === 'table' && block.headers?.length) {
        const headers = block.headers.map(h => '<th>' + escapeHtml(h) + '</th>').join('');
        const rows = (block.rows || []).map(row => '<tr>' + row.map(c => '<td>' + escapeHtml(c) + '</td>').join('') + '</tr>').join('');
        parts.push('<table><thead><tr>' + headers + '</tr></thead><tbody>' + rows + '</tbody></table>');
      }
    }
  }
  return parts.length ? parts.join('') : '<p>No content available.</p>';
}

function formatParagraphsToHtml(text) {
  if (!text || !String(text).trim()) return '';
  const lines = String(text).split('\n');
  const out = [];
  for (const line of lines) {
    const t = line.trim();
    if (t) out.push('<p>' + escapeHtml(t) + '</p>');
  }
  return out.join('');
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function httpsGet(url) {
  return new Promise((resolve, reject) => {
    const req = https.get(url, { timeout: 20000 }, (res) => {
      let body = '';
      res.on('data', chunk => { body += chunk; });
      res.on('end', () => {
        if (res.statusCode !== 200) {
          reject(new Error(`HTTP ${res.statusCode}: ${url}`));
          return;
        }
        try {
          resolve(JSON.parse(body));
        } catch {
          reject(new Error('Invalid JSON: ' + url));
        }
      });
    });
    req.on('error', reject);
    req.on('timeout', () => { req.destroy(); reject(new Error('Timeout: ' + url)); });
  });
}

const FETCH_RETRIES = 3;
const FETCH_RETRY_DELAY_MS = 1000;

async function httpsGetWithRetry(url) {
  let lastErr;
  for (let attempt = 1; attempt <= FETCH_RETRIES; attempt++) {
    try {
      return await httpsGet(url);
    } catch (err) {
      lastErr = err;
      if (attempt < FETCH_RETRIES) {
        console.warn(`Attempt ${attempt}/${FETCH_RETRIES} failed: ${err.message}. Retrying in ${FETCH_RETRY_DELAY_MS}ms...`);
        await sleep(FETCH_RETRY_DELAY_MS);
      }
    }
  }
  throw lastErr;
}

function loadManifest() {
  try {
    const raw = fs.readFileSync(manifestPath, 'utf8');
    const data = JSON.parse(raw);
    return typeof data === 'object' && data !== null ? data : {};
  } catch {
    return {};
  }
}

function saveManifest(manifest) {
  fs.writeFileSync(manifestPath, JSON.stringify(manifest, null, 2), 'utf8');
}

function buildRedirectPage(slug) {
  const target = '/blog/' + encodeURI(slug) + '/';
  return (
    '<!DOCTYPE html><html lang="en"><head><meta charset="UTF-8">' +
    '<meta http-equiv="refresh" content="0;url=' + escapeHtml(target) + '">' +
    '<title>Redirect</title></head><body>' +
    '<script>window.location.replace("' + target.replace(/"/g, '\\"') + '");</script>' +
    '<p>Redirecting to <a href="' + escapeHtml(target) + '">article</a>...</p></body></html>'
  );
}

function buildStaticMainHtml(article, bodyHtml, slug) {
  const title = article.title || 'Article';
  const author = escapeHtml(article.author || 'SeaDays');
  const dateStr = formatDisplayDate(article.published_at ?? article.publishedAt ?? article.timestamp);
  const heroUrl = (article.heroImageUrl || article.thumbnailUrl || article.ogImage || '').trim();
  const heroImg = heroUrl
    ? `<img src="${escapeHtml(heroUrl)}" alt="${escapeHtml(title)}" class="article-hero-image" loading="lazy" decoding="async">`
    : '';

  return (
    `<article id="articleContent">
${GENERATED_MARKER}
        <a href="/blog/" class="back-to-blog">← Back to Blog</a>
        <div class="article-hero">
            <h1 id="articleTitle">${escapeHtml(title)}</h1>
            <div class="article-meta">
                <span class="author" id="articleAuthor">${author}</span>
                <span id="articleDate">${dateStr}</span>
            </div>
            ${heroImg}
        </div>
        <div id="articleBody" class="article-body">${bodyHtml}</div>
    </article>`
  );
}

function injectIntoTemplate(templateHtml, article, slug, bodyHtml) {
  const title = escapeHtml(article.title || 'Article');
  const excerpt = escapeHtml(
    (article.excerpt || article.seoDescription || article.metaDescription || '').trim().slice(0, 160) || title
  );
  const canonicalUrl = `${BASE_URL}/blog/${slug}`;
  const ogImage = (article.ogImage || article.heroImageUrl || article.thumbnailUrl || '').trim()
    || 'https://auth.seadays.app/storage/v1/object/public/SeadaysPublic/seadaysfav.png';
  const publishedIso = toPublishedIso(article);

  const jsonLd = {
    '@context': 'https://schema.org',
    '@type': 'BlogPosting',
    headline: title,
    description: excerpt,
    author: { '@type': 'Organization', name: SITE_NAME },
    publisher: { '@type': 'Organization', name: SITE_NAME },
    datePublished: publishedIso || undefined,
    mainEntityOfPage: canonicalUrl,
  };

  let out = templateHtml;

  out = out.replace(/<title>[^<]*<\/title>/, `<title>${title} | ${SITE_NAME}</title>`);
  out = out.replace(
    /<meta name="description" content="[^"]*"/,
    `<meta name="description" content="${excerpt}"`
  );
  out = out.replace(
    /<link rel="canonical" href="[^"]*"/,
    `<link rel="canonical" href="${canonicalUrl}"`
  );

  const ogTitle = out.includes('property="og:title"')
    ? out.replace(/<meta property="og:title" content="[^"]*"/, `<meta property="og:title" content="${title}"`)
    : out.replace('</head>', `  <meta property="og:title" content="${title}">\n</head>`);
  out = ogTitle;

  const ogDesc = out.replace(/<meta property="og:description" content="[^"]*"/, `<meta property="og:description" content="${excerpt}"`);
  out = ogDesc;

  out = out.replace(/<meta property="og:url" content="[^"]*"/, `<meta property="og:url" content="${canonicalUrl}"`);
  out = out.replace(/<meta property="og:image" content="[^"]*"/, `<meta property="og:image" content="${ogImage}"`);
  if (!out.includes('property="og:type"')) {
    out = out.replace('</head>', `  <meta property="og:type" content="article">\n</head>`);
  }

  out = out.replace(/<meta property="twitter:card" content="[^"]*"/, '<meta property="twitter:card" content="summary_large_image">');
  out = out.replace(/<meta property="twitter:url" content="[^"]*"/, `<meta property="twitter:url" content="${canonicalUrl}"`);
  out = out.replace(/<meta property="twitter:title" content="[^"]*"/, `<meta property="twitter:title" content="${title}"`);
  out = out.replace(/<meta property="twitter:description" content="[^"]*"/, `<meta property="twitter:description" content="${excerpt}"`);
  out = out.replace(/<meta property="twitter:image" content="[^"]*"/, `<meta property="twitter:image" content="${ogImage}"`);

  const jsonLdScript = `<script type="application/ld+json">\n${JSON.stringify(jsonLd)}\n</script>`;
  if (out.includes('application/ld+json')) {
    out = out.replace(/<script type="application\/ld\+json">[\s\S]*?<\/script>/, jsonLdScript);
  } else {
    out = out.replace('</head>', `  ${jsonLdScript}\n</head>`);
  }

  const staticMain = buildStaticMainHtml(article, bodyHtml, slug);
  const mainStart = '<main class="container">';
  const mainEnd = '</main>';
  const startIdx = out.indexOf(mainStart);
  const endIdx = out.indexOf(mainEnd, startIdx);
  if (startIdx !== -1 && endIdx !== -1 && endIdx > startIdx) {
    out = out.slice(0, startIdx + mainStart.length) + '\n        ' + staticMain + '\n        ' + out.slice(endIdx);
  }

  return out;
}

function writeSitemap(blogDirPath, repoRootPath) {
  const staticUrls = [
    { loc: BASE_URL + '/', lastmod: new Date().toISOString().split('T')[0] },
    { loc: BASE_URL + '/index.html', lastmod: new Date().toISOString().split('T')[0] },
    { loc: BASE_URL + '/blog/', lastmod: new Date().toISOString().split('T')[0] },
  ];

  let xml = '<?xml version="1.0" encoding="UTF-8"?>\n<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n';
  for (const u of staticUrls) {
    xml += `  <url><loc>${escapeXml(u.loc)}</loc><lastmod>${u.lastmod}</lastmod></url>\n`;
  }

  if (fs.existsSync(blogDirPath)) {
    const entries = fs.readdirSync(blogDirPath, { withFileTypes: true });
    const dirs = entries.filter(d => d.isDirectory());
    for (const d of dirs) {
      const slug = d.name;
      const indexPath = path.join(blogDirPath, slug, 'index.html');
      if (!fs.existsSync(indexPath)) continue;
      const stat = fs.statSync(indexPath);
      const lastmod = stat.mtime ? new Date(stat.mtime).toISOString().split('T')[0] : new Date().toISOString().split('T')[0];
      const loc = BASE_URL + '/blog/' + slug;
      xml += `  <url><loc>${escapeXml(loc)}</loc><lastmod>${lastmod}</lastmod></url>\n`;
    }
  }

  xml += '</urlset>';
  fs.writeFileSync(path.join(repoRootPath, 'sitemap.xml'), xml, 'utf8');
  console.log('Wrote sitemap.xml');
}

async function main() {
  if (!fs.existsSync(templatePath)) {
    console.error('Template not found:', templatePath);
    process.exit(1);
  }

  const templateHtml = fs.readFileSync(templatePath, 'utf8');
  let data;
  try {
    data = await httpsGetWithRetry(API_BASE);
  } catch (err) {
    console.error('ERROR: Blog API unavailable. Skipping generation.');
    console.error(err.message);
    process.exit(0);
  }

  const rawList = Array.isArray(data) ? data : (data?.articles || []);
  const articles = rawList.filter(a => a && (a.isDraft !== true && a.showOnWebsite !== false));

  const slugSet = new Set();
  const bySlug = [];
  for (const a of articles) {
    let slug = (a.slug || '').trim();
    if (!slug) slug = slugify(a.title || '');
    if (!slug) {
      console.log('Skip (no slug):', (a.title || a.id || 'unknown').slice(0, 50));
      continue;
    }
    let base = slug;
    let n = 1;
    while (slugSet.has(slug)) {
      slug = base + '-' + n;
      n++;
    }
    slugSet.add(slug);
    bySlug.push({ ...a, slug });
  }

  fs.mkdirSync(blogDir, { recursive: true });
  const manifest = loadManifest();
  let generated = 0;
  let skipped = 0;

  for (const article of bySlug) {
    const articleDir = path.join(blogDir, article.slug);
    const indexPath = path.join(articleDir, 'index.html');
    const redirectPath = path.join(blogDir, article.slug + '.html');
    const existingContent = fs.existsSync(indexPath) ? fs.readFileSync(indexPath, 'utf8') : '';
    const isManual = existingContent && !existingContent.includes(GENERATED_MARKER);
    if (isManual) {
      console.log('Skip (manual file): blog/' + article.slug + '/index.html');
      skipped++;
      continue;
    }

    const articleUpdated = toUpdatedAtMs(article);
    const lastGenerated = manifest[article.slug];
    if (lastGenerated != null && articleUpdated <= lastGenerated) {
      console.log('Skip (unchanged): blog/' + article.slug + '/index.html');
      skipped++;
      continue;
    }

    let full = article;
    if (!article.content && !article.structuredContent && article.id) {
      try {
        const single = await httpsGet(`${API_BASE}/${encodeURIComponent(article.id)}`);
        const art = single?.article ?? single;
        if (art) full = { ...article, ...art };
      } catch {
        // use list data only
      }
    }

    const bodyHtml = getBodyHtml(full);
    const html = injectIntoTemplate(templateHtml, full, article.slug, bodyHtml);
    fs.mkdirSync(articleDir, { recursive: true });
    fs.writeFileSync(indexPath, html, 'utf8');
    fs.writeFileSync(redirectPath, buildRedirectPage(article.slug), 'utf8');
    manifest[article.slug] = articleUpdated;
    generated++;
    console.log('Generated: blog/' + article.slug + '/index.html');
    console.log('Redirect: blog/' + article.slug + '.html');
  }

  saveManifest(manifest);
  writeSitemap(blogDir, repoRoot);
  console.log('Done. Generated:', generated, 'Skipped:', skipped);
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
